package cn.zup.formdesign.entity;

public class FormControl {
	private Integer id;
	private String name;
	private String title;
	private String plugins;
	private String fildname;
	private String orgwidth;
	private String orgalign;
	private String orgtype;
	private String style;
	private String content;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPlugins() {
		return plugins;
	}
	public void setPlugins(String plugins) {
		this.plugins = plugins;
	}
	public String getFildname() {
		return fildname;
	}
	public void setFildname(String fildname) {
		this.fildname = fildname;
	}
	public String getOrgwidth() {
		return orgwidth;
	}
	public void setOrgwidth(String orgwidth) {
		this.orgwidth = orgwidth;
	}
	public String getOrgalign() {
		return orgalign;
	}
	public void setOrgalign(String orgalign) {
		this.orgalign = orgalign;
	}
	public String getOrgtype() {
		return orgtype;
	}
	public void setOrgtype(String orgtype) {
		this.orgtype = orgtype;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
	
}
